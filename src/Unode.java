
public class Unode {
	public double value;
	public double weight;
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	public Unode(double value, double weight) {
		super();
		this.value = value;
		this.weight = weight;
	}

}
